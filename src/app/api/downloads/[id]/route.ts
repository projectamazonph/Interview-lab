        sheet.addRow({ metric: '', value: '', result: '' });
        sheet.addRow({ metric: 'ROAS Calculator', value: '', result: '' });
        sheet.addRow({ metric: 'Ad Revenue ($)', value: 0, result: 'Enter your ad revenue' });
        sheet.addRow({ metric: 'Ad Spend ($)', value: 0, result: 'Enter your ad spend' });
        sheet.addRow({ metric: 'ROAS (x)', value: '', result: '=IF(B10>0,B9/B10,"N/A")' });
