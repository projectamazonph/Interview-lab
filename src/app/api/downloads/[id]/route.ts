// Access tier hierarchy: free < starter < pro
const TIER_HIERARCHY: Record<string, number> = { free: 0, starter: 1, pro: 2 };

export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  try {
    const { id } = await params;
    const download = await db.download.findUnique({ where: { id } });

    if (!download) {
      return NextResponse.json({ error: 'Download not found' }, { status: 404 });
    }

    // Validate fileName contains only safe characters
    const safeFileNamePattern = /^[a-zA-Z0-9._-]+$/;
    if (!safeFileNamePattern.test(download.fileName)) {
      return NextResponse.json({ error: 'Invalid file name' }, { status: 400 });
    }

    // Verify the user is authenticated and has sufficient access tier
    const user = await getUserFromRequest(request);
    if (!user) {
      return NextResponse.json({ error: 'Authentication required to download files' }, { status: 401 });
    }

    const userTierLevel = TIER_HIERARCHY[user.subscriptionTier] ?? 0;
    const requiredTierLevel = TIER_HIERARCHY[download.accessTier] ?? 0;
    if (userTierLevel < requiredTierLevel) {
      return NextResponse.json({ error: `This resource requires ${download.accessTier} tier or higher` }, { status: 403 });
    }

    // Increment download count
    await db.download.update({
      where: { id },
      data: { downloadCount: { increment: 1 } },
    });

    // Generate file dynamically based on metadata
    const { title, fileType, category, description, role } = download;

    if (fileType === 'PDF') {
      return await generatePdf(title, description || '', category, role);
    } else if (fileType === 'DOCX') {
      return await generateDocx(title, description || '', category, role);
    } else if (fileType === 'XLSX') {
      return await generateXlsx(title, description || '', category, role);
    }

    // Fallback: generate plain text
    const textContent = buildTextContent(title, description || '', category, role);
    return new NextResponse(textContent, {
      headers: {
        'Content-Type': 'text/plain',
        'Content-Disposition': `attachment; filename="${download.fileName.replace(/\.[^.]+$/, '.txt')}"`,
      },
    });
  } catch (error) {
    console.error('Download GET error:', error);
    return NextResponse.json({ error: 'Failed to process download' }, { status: 500 });
  }
}

function buildTextContent(title: string, description: string, category: string, role: string): string {
  const lines: string[] = [];
  lines.push(title);
  lines.push('='.repeat(title.length));
  lines.push('');
  lines.push(`Category: ${category}`);
  lines.push(`Role: ${role}`);
  lines.push('');
  if (description) {
    lines.push(description);
    lines.push('');
  }

  switch (category) {
    case 'Cheat Sheets':
      lines.push('QUICK REFERENCE');
      lines.push('-'.repeat(16));
      lines.push('');
      addCheatSheetContent(lines, role);
      break;
    case 'Checklists':
      lines.push('CHECKLIST');
      lines.push('-'.repeat(10));
      lines.push('');
      addChecklistContent(lines, role);
      break;
    case 'Calculators':
      lines.push('CALCULATOR WORKSHEET');
      lines.push('-'.repeat(21));
      lines.push('');
      addCalculatorContent(lines, role);
      break;
    case 'Resume Templates':
      lines.push('RESUME TEMPLATE');
      lines.push('-'.repeat(16));
      lines.push('');
      addResumeTemplateContent(lines, role);
      break;
    case 'Cover Letters':
      lines.push('COVER LETTER TEMPLATE');
      lines.push('-'.repeat(21));
      lines.push('');
      addCoverLetterContent(lines, role);
      break;
    case 'Plans & Reports':
      lines.push('PLAN / REPORT TEMPLATE');
      lines.push('-'.repeat(22));
      lines.push('');
      addPlanReportContent(lines, role);
      break;
    default:
      lines.push('NOTES');
      lines.push('-'.repeat(5));
      lines.push('');
      lines.push('[Your notes here]');
  }

  lines.push('');
  lines.push('---');
  lines.push('Generated by Amazon VA Interview Lab');
  return lines.join('\n');
}

