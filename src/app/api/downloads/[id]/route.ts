// ---- PDF Generation ----
async function generatePdf(title: string, description: string, category: string, role: string) {
  try {
    // Generate PDF manually to avoid pdfkit font resolution issues in Next.js bundled environment.
    // pdfkit requires AFM font files resolved via __dirname which doesn't work in Next.js.
    const contentLines = getCategoryContent(category, role);
    const pdfContent = buildSimplePdf(title, description, category, role, contentLines);

    const safeName = title.replace(/[^a-zA-Z0-9]/g, '_').substring(0, 50);
    return new NextResponse(new Uint8Array(pdfContent), {
      headers: {
        'Content-Type': 'application/pdf',
        'Content-Disposition': `attachment; filename="${safeName}.pdf"`,
      },
    });
  } catch (error) {
    console.error('PDF generation error:', error);
    // Fallback to text
    const textContent = buildTextContent(title, description, category, role);
    return new NextResponse(textContent, {
      headers: {
        'Content-Type': 'text/plain',
        'Content-Disposition': `attachment; filename="${title.replace(/[^a-zA-Z0-9]/g, '_')}.txt"`,
      },
    });
  }
}

// Build a minimal valid PDF manually (no external font files needed)
function buildSimplePdf(title: string, description: string, category: string, role: string, contentLines: string[]): Buffer {
  // Collect all lines for the PDF
  const allLines: Array<{ text: string; size: number; bold: boolean; indent: number; color: string }> = [];

  // Title
  allLines.push({ text: title, size: 24, bold: true, indent: 0, color: '0 0 0' });
  allLines.push({ text: `${category}  \u2022  ${role}`, size: 10, bold: false, indent: 0, color: '0.4 0.4 0.4' });
  allLines.push({ text: '', size: 6, bold: false, indent: 0, color: '0 0 0' });

  if (description) {
    allLines.push({ text: description, size: 11, bold: false, indent: 0, color: '0.2 0.2 0.2' });
    allLines.push({ text: '', size: 6, bold: false, indent: 0, color: '0 0 0' });
  }

  for (const line of contentLines) {
    if (line.startsWith('## ')) {
      allLines.push({ text: '', size: 4, bold: false, indent: 0, color: '0 0 0' });
      allLines.push({ text: line.replace('## ', ''), size: 14, bold: true, indent: 0, color: '0 0 0' });
    } else if (line.startsWith('# ')) {
      allLines.push({ text: '', size: 4, bold: false, indent: 0, color: '0 0 0' });
      allLines.push({ text: line.replace('# ', ''), size: 16, bold: true, indent: 0, color: '0 0 0' });
    } else if (line.startsWith('- [ ] ')) {
      allLines.push({ text: `\u2610  ${line.replace('- [ ] ', '')}`, size: 11, bold: false, indent: 20, color: '0 0 0' });
    } else if (line.startsWith('- ')) {
      allLines.push({ text: `\u2022  ${line.replace('- ', '')}`, size: 11, bold: false, indent: 20, color: '0 0 0' });
    } else if (line.trim() === '') {
      allLines.push({ text: '', size: 4, bold: false, indent: 0, color: '0 0 0' });
    } else {
      allLines.push({ text: line, size: 11, bold: false, indent: 0, color: '0 0 0' });
    }
  }

  allLines.push({ text: '', size: 8, bold: false, indent: 0, color: '0 0 0' });
  allLines.push({ text: 'Generated by Amazon VA Interview Lab', size: 8, bold: false, indent: 0, color: '0.6 0.6 0.6' });

  // Build PDF structure
  const objects: string[] = [];
  let objectCount = 0;

  const addObject = (content: string): number => {
    objectCount++;
    objects.push(`${objectCount} 0 obj\n${content}\nendobj`);
    return objectCount;
  };

  // Object 1: Catalog
  const catalogObj = addObject('<< /Type /Catalog /Pages 2 0 R >>');

  // Object 2: Pages (will reference page objects)
  const pagesObj = addObject('<< /Type /Pages /Kids [3 0 R] /Count 1 >>');

  // Build page content stream
  let streamContent = '';
  let y = 770; // Start near top of page

  for (const item of allLines) {
    if (y < 50) break; // Stop if we run off the page

    const fontName = item.bold ? '/F2' : '/F1';
    const x = 50 + item.indent;

    // Escape special PDF characters
    const escapedText = item.text
      .replace(/\\/g, '\\\\')
      .replace(/\(/g, '\\(')
      .replace(/\)/g, '\\)');

    streamContent += `BT\n${fontName} ${item.size} Tf\n${item.color} rg\n${x} ${y} Td\n(${escapedText}) Tj\nET\n`;
    y -= item.size + 4;
  }

  // Object 3: Page
  const streamObjId = addObject(`<< /Length ${streamContent.length} >>\nstream\n${streamContent}\nendstream`);

  addObject(`<< /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] /Contents ${streamObjId} 0 R /Resources << /Font << /F1 4 0 R /F2 5 0 R >> >> >>`);

  // Object 4: Font (Courier)
  addObject('<< /Type /Font /Subtype /Type1 /BaseFont /Courier /Encoding /WinAnsiEncoding >>');

  // Object 5: Font (Courier-Bold)
  addObject('<< /Type /Font /Subtype /Type1 /BaseFont /Courier-Bold /Encoding /WinAnsiEncoding >>');

  // Build PDF
  let pdf = '%PDF-1.4\n';
  const offsets: number[] = [];

  for (let i = 0; i < objects.length; i++) {
    offsets.push(pdf.length);
    pdf += `${objects[i]}\n`;
  }

  // Cross-reference table
  const xrefOffset = pdf.length;
  pdf += `xref\n0 ${objectCount + 1}\n`;
  pdf += '0000000000 65535 f \n';
  for (const offset of offsets) {
    pdf += `${offset.toString().padStart(10, '0')} 00000 n \n`;
  }

  pdf += `trailer\n<< /Size ${objectCount + 1} /Root ${catalogObj} 0 R >>\n`;
  pdf += `startxref\n${xrefOffset}\n%%EOF`;

  return Buffer.from(pdf, 'binary');
}

// ---- DOCX Generation ----
async function generateDocx(title: string, description: string, category: string, role: string) {
  try {
    const children: Paragraph[] = [];

    // Title
    children.push(new Paragraph({
      children: [new TextRun({ text: title, bold: true, size: 36 })],
      heading: HeadingLevel.HEADING_1,
      alignment: AlignmentType.CENTER,
    }));

    // Category & Role
    children.push(new Paragraph({
      children: [new TextRun({ text: `${category}  •  ${role}`, size: 18, color: '666666' })],
      alignment: AlignmentType.CENTER,
    }));

    children.push(new Paragraph({ text: '' }));

    // Description
    if (description) {
      children.push(new Paragraph({
        children: [new TextRun({ text: description, size: 22 })],
      }));
      children.push(new Paragraph({ text: '' }));
    }

    // Category-specific content
    const contentLines = getCategoryContent(category, role);
    for (const line of contentLines) {
      if (line.startsWith('## ')) {
        children.push(new Paragraph({
          children: [new TextRun({ text: line.replace('## ', ''), bold: true, size: 26 })],
          heading: HeadingLevel.HEADING_2,
        }));
      } else if (line.startsWith('# ')) {
        children.push(new Paragraph({
          children: [new TextRun({ text: line.replace('# ', ''), bold: true, size: 28 })],
          heading: HeadingLevel.HEADING_1,
        }));
      } else if (line.startsWith('- [ ] ')) {
        children.push(new Paragraph({
          children: [new TextRun({ text: `☐  ${line.replace('- [ ] ', '')}`, size: 22 })],
          indent: { left: 360 },
        }));
      } else if (line.startsWith('- ')) {
        children.push(new Paragraph({
          children: [new TextRun({ text: `•  ${line.replace('- ', '')}`, size: 22 })],
          indent: { left: 360 },
        }));
      } else if (line.trim() === '') {
        children.push(new Paragraph({ text: '' }));
      } else {
        children.push(new Paragraph({
          children: [new TextRun({ text: line, size: 22 })],
        }));
      }
    }

    // Footer
    children.push(new Paragraph({ text: '' }));
    children.push(new Paragraph({
      children: [new TextRun({ text: 'Generated by Amazon VA Interview Lab', size: 16, color: '999999', italics: true })],
      alignment: AlignmentType.CENTER,
    }));

    const doc = new Document({
      sections: [{ properties: {}, children }],
    });

    const buffer = await Packer.toBuffer(doc);
    const safeName = title.replace(/[^a-zA-Z0-9]/g, '_').substring(0, 50);

    return new NextResponse(new Uint8Array(buffer), {
      headers: {
        'Content-Type': 'application/vnd.openxmlformats-officedocument.wordprocessingml.document',
        'Content-Disposition': `attachment; filename="${safeName}.docx"`,
      },
    });
  } catch (error) {
    console.error('DOCX generation error:', error);
    // Fallback to text
    const textContent = buildTextContent(title, description, category, role);
    return new NextResponse(textContent, {
      headers: {
        'Content-Type': 'text/plain',
        'Content-Disposition': `attachment; filename="${title.replace(/[^a-zA-Z0-9]/g, '_')}.txt"`,
      },
    });
  }
}

