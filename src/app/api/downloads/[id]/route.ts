        sheet.addRow({ metric: 'Break-even ACoS', value: '', result: '' });
        sheet.addRow({ metric: 'Product Price ($)', value: 0, result: 'Enter product price' });
        sheet.addRow({ metric: 'Product Cost ($)', value: 0, result: 'Enter product cost' });
        sheet.addRow({ metric: 'FBA Fees ($)', value: 0, result: 'Enter FBA fees' });
